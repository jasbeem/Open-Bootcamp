def suma(a,b):
    """
    Suma los parametro a,b y devuelve el resultado
    """
    return a+b

def resta(a,b):
    """
    Resta los parametro a,b y devuelve el resultado
    """
    return a-b

def multiplica(a,b):
    """
    Multiplica los parametro a,b y devuelve el resultado
    """
    return a*b

def divide(a,b):
    """
    Divide los parametro a,b y devuelve el resultado
    """
    return a/b
